        <p>Moving to the United States is, for most, a big decision. Therefore, it’s crucial to be well prepared when you apply for the Diversity Visa Program in the United States. We have made a list below of the most common questions we get from our applicants from all over the world. If you can’t find your answer below, we have a vast <a href="">knowledge database</a> where you can search and find most answers related to the Green Card Program. If you can’t find your answer, you are always welcome to contact our support directly by <a href="">submitting a support ticket</a>. Our immigration team is ready to assist you 365 days a year, and most support tickets are answered within a few hours. We are here to help you, living your dream, and moving to the United States.</p>

        <h4>What is a Green Card?</h4>
    
        <p>A Green Card is a Permanent Residence Visa of the U.S.A. A Green Card will give you legal right to work and live permanently in the United States. Green Card holders receive health, education, and several other benefits. If you win a Green Card, you can apply for U.S. Citizenship at a later time. The Green Card does not affect your present citizenship. You and your family could be lucky winners if you act immediately!</p>
      
        <h4>Who is eligible to apply in the Green Card lottery?</h4>

        <p>To enter, an applicant must be a native of an eligible country (see below), and MUST have EITHER a high school education or its equivalent; OR two years of work experience within the past five years in an occupation requiring at least two years of training or experience (these requirements are explained in details below).</p>

        <h4>If a person does not meet these requirements, he/she should NOT submit an entry to the Visa Lottery program.</h4>

        <p>You are eligible wherever you are living. You do not have to speak English. You do not need to have relatives in the USA.</p>

        <h4>What are the requirements for education or work experience?</h4>
    
        <p>The law and regulations require that every applicant must have at least a high school education or its equivalent or , within the past five years, have two years of work experience in an occupation requiring at least two years of training or experience. A "high school education or equivalent" is defined as successful completion of a twelve-year course of elementary and secondary education in the United States or successful completion in another country of a formal course of elementary and secondary education comparable to a high school education in the United States. Documentary proof of education or work experience should not be submitted with the lottery application, but must be presented to the consular officer at the time of the immigrant visa interview. Check if your occupation or work experience qualify for the Diversity Visa Program please visit www.onetonline.org</p>

        <h4>Must I re-apply every year for the Green Card Lottery to be considered?</h4>

        <p>The law specifies there must be a separate application for each year's visa lottery. The fact that a person has registered for Visa Lotteries in the past has no effect on the next Visa Lottery. Each person who wishes to be included in next Lottery must submit a new application.</p>

        <h4>How is the term "native" defined? Are there any bases upon which persons who have not been born in a qualifying country may be eligible to enter?</h4>
    
        <p>"Native" ordinarily means someone born within a particular country, regardless of the individual's current country of residence or nationality. According to Section 202(b) of the Immigration and Nationality Act, if a person was born in an ineligible country but his/her spouse was born in an eligible country, such person can claim the spouse's country of birth rather than his/her own. For example, a person born in an excluded country such as England may still apply if his or her spouse was born in a qualifying country. Also, if a person was born in an ineligible country, but neither of his/her parents was born there or resided there at the time of the birth, such person may be able to claim nativity in one of the parents' country of birth. For example, if one of your parents was born in France and the other was born in Germany, and you were born in Canada while they were visiting Canada, but had not established residency; you could claim France or Germany as your qualifying country.</p>
    
        <h4>Why do certain countries not qualify for the Green Card Lottery (Diversity Visa Program?</h4>

        <p>Diversity visas are intended to provide an immigration opportunity for persons from countries other than the main source countries of immigration to the U.S. The law states that no diversity visas shall be provided for "high admission" countries, that is, countries from which during the previous five years there were more than 55,000 immigrants in the Family-Sponsored and Employment-Based visa categories. The list of countries, which do not qualify for the Green Card Lottery, is subject to change from one year to the next.</p>
    
        <h4>Is each applicant limited to only one application in the Green Card Lottery?</h4>

        <p>Yes, if otherwise qualified, a husband and a wife may each submit one Green Card lottery application. If you receive a Green Card through the lottery, your spouse and unmarried children under 21 will also get green cards at the same time as you. Unmarried children under 21 may be included on each of their parents' application forms. All children over 21 years of age must file separate applications. Submitting separate applications for you and your spouse double your chances of winning.</p>

        <h4>Is there a minimum age to apply for the lottery?</h4>

        <p>There is no minimum age to apply for the visa lottery. However, the requirement of a high school education or work experience for each principal applicant at the time of visa issuance will effectively disqualify most persons who are under age 18.</p>

        <h4>How does the Green Card Lottery work?</h4>

        <p>At the National Visa Center all entries received will be separated into one of six geographic regions and individually numbered. After the end of the application period, a computer will randomly select cases from among all the mail received for each geographic region. Within each region, the first letter randomly selected will be the first case registered, the second letter selected the second registration, etc. When a case has been registered, the applicant will immediately be sent a notification letter by the National Visa Center, which will provide appropriate visa application instructions. Note: Entries that do not have all required information will be rejected. Also, entries received before or after the announced dates will be disqualified.</p>

        <h4>How would I know if I win the Green Card Lottery?</h4>

        <p>Applicants will be selected at random by computer from among all qualified entries. Only winners will be notified by mail at the address listed on their application. The notifications will be sent to the winners between April and July, along with instructions on how to apply for an immigrant visa. Persons not selected will NOT be notified. U.S. embassies and consulates will not be able to provide a list of successful applicants. Anyone who does NOT receive a letter will know that his/her application has not been selected.</p>

        <h4>May persons who are in the U.S. apply?</h4>

        <p>Yes, an applicant may be in the U.S. or in another country, and the application may be mailed in the U.S. or abroad.</p>

        <h4>May persons who are already registered for an immigrant visa in another Category apply?</h4>

        <p>Yes, such persons may enter the lottery.</p>

        <h4>What is my chances of winning?</h4>

        <p>The winning chances cannot be determined at this time. It will depend on the number of applications to be submitted.</p>

        <h4>Where is the Lottery held?</h4>

        <p>At the National Visa Center in Portsmouth, New Hampshire.</p>

        <h4>What is the cost for entering the Lottery?</h4>

        <p>Our processing fee is only $80.00 for one application, and only $120.00 for a married couple submitting two separate applications (one for husband and one for wife).</p>

        <h4>What does Free Transport offer include?</h4>

        <p>If you order this service from US Green Card Office . and you win the Lottery all your expenses from moving will be paid.. The service includes transport of all family members, pets (if they are allowed in the U.S.A.) and all furniture. It does not include transport of any vehicles.</p>

    
    
    
    
    
