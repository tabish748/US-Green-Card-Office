        <p>What does this video mean for Americans, and for those wanting to take advantage of their generous Green Card program? That’s what we’ll take a look at in this article.</p>
        <br>

        <h3>Time to Heal</h3>
        
        <p>One of the first executive orders Biden signed was to reverse immigration bans on Muslim-majority countries. This was a cornerstone of the Trump administration, and arguably one of their most controversial and problematic policies.</p>
        <br>

        <p>By claiming now is the time to heal, it’s clear the Biden administration plans to take a vastly different approach to immigration. On Biden’s website, the president acknowledges that the USA is a country of immigrants, and this will be the basis behind their future policies. Diversity preferences are essential to preserving a robust and vibrant immigration system. As president Biden says, I will make sure our core values of immigration are preserved and ensure the Diversity Visa Program also know as the Green Card Lottery will be an even greater part of our immigration system so immigrants from all over the world have the chance to legally become U.S. citizens in the future.</p>
        <br>

        <p>But part of this overhaul will be healing the wounds caused by Trump. It’s no secret that his stance on immigration put many off applying for a Green Card, whether through the Green Card Lottery or not. As a result, the Biden administration will have to heal America’s image in the eyes of the rest of the world.</p>
        <br>
            
        <h3>Time to Improve</h3>

        <p>The video also claims that it’s time to improve. This of course is a general statement about the current condition of America, as it’s clear there are a number of core issues affecting the country.</p>
        <br>

        <p>However, one key area Biden is looking to improve is the immigration system. As vice president to Obama, Biden supported the DACA program and spearheaded an overhaul of the country’s immigration process. Unfortunately, this didn’t even reach a vote in the Republican-controlled Senate and was obviously scrapped entirely under Trump.</p>
        <br>

        <p>Now that Biden is president and has control over both Houses, it’s entirely possible for the administration to finish its work. This will include overhauling the current system to make it faster and more modern, while also addressing the causes of mass migration from areas of Central America. These include violence, economic instability, and corruption, all of which can be improved through targeted state aid.</p>
        <br>

        <h3>Time to Recover</h3>

        <p>America has plenty to recover from; this is hardly a secret. Its immigration policies are definitely an area that requires recovery. While the <a href="">Diversity Visa Program <a> hasn’t been significantly affected during the Trump administration, their policies have had an overall impact on immigration levels.</p>
        <br>

        <p>Hopefully now is the time for America to recover from its hostile position towards immigration. Biden and Harris couldn’t be more opposite to Trump in their perception of immigration and its value. It’s now certain that the Green Card Lottery will continue and will most likely even be expand from 50,000 to 80,000 available Green Cards each year over the next 4 years.</p>
        <br>
            
        <h3>Time to Reconcile</h3>

        <p>Similarly, the administration acknowledges that it’s time to reconcile. One key area that requires reconciliation is Trump’s southern border policies, most notably his inhumane push to separate immigrant children from their parents at the border, many of which still haven’t been reunited.</p>
        <br>
    
        <p>Another area that requires reconciliation is the naturalization process for Green Card holders. The Biden administration states that Trump made this system purposefully difficult to deter settlers from becoming naturalized American citizens. Naturalization is a key step in the migration process, and grants Green Card holders full rights, including the right to vote.</p>    
        <br>

        <p>Currently, this process is long-winded and expensive, which puts many applicants off. What’s more, it implies Green Card holders aren’t welcome in the country once their visa has expired, a perception that needs to change.</p>
        <br>

        <p>Biden’s plan is to streamline the system so it’s simple, effective, and modern. Doing so will allow them to address the backlog of applicants while also making the process simpler for people in the future. Hopefully this will reconcile differences and restore hope in the process.</p>
        <br>

        <h3>What Does this Mean for Immigrants?</h3>

        <p>The general message of Biden’s video is that now is the time to move forward. To do so, the country must heal, listen, adapt, and support. Whatever your views on Trump, it’s not a lie that his policies and stance on many issues, particularly immigration and climate change, impacted the way America was viewed by the world.</p>
        <br>

        <p>Biden’s plan is to move away from this image and to restore faith in America. It’s still the land of opportunities for anyone wanting freedom and a better quality of life, but it’s time to make that known to potential immigrants worldwide.</p>
        <br>

        <p>Hopefully Biden’s first executive orders show his administration’s vastly different stance on immigration. The Democrats have always been more pro-immigration than the Republicans, but Biden’s administration appears to be more so than even Obama’s.</p>
        <br>

        <p>Streamlining and modernizing the immigration system shows that Biden favors immigrants and the value they bring to America. This is true not only for their direct American neighbors, but for all countries. Just look at Biden reversing the Muslim-majority ban on his first day in office.</p>
        <br>

        <p>The same is true for countries eligible for the Diversity Visa Program. It’s guaranteed that the Biden administration will favor the Green Card Lottery with their immigration reforms, as they are clearly in favor of diversity among the American population.</p>
