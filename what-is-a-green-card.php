<h3>Employment-Based Green Cards</h3>

<p>Foreign nationals may be able to get a Green Card to stay in the United States if they receive an offer of permanent employment from an employer within the United States and if that employer is willing to sponsor them for a Green Card. Those with higher education and ability are more likely to receive an employment-based Green Card for entry into the United States.</p>

<h3>Investment-Based Green Cards</h3>

<p>Foreign nationals can gain a Green Card for the United States by creating jobs in the country through investing between $500,000 and $1,000,000 in commercial enterprises. The investment must benefit the U.S. economy, and the national in question must have created at least ten full-time jobs for American citizens.</p>

<h3>Refugee/Asylum</h3>

<p>People who are granted asylum or refugee status within the United States are eligible for a Green Card if they meet requirements. They can file an application for an adjustment of status and receive permanent resident status within the U.S. The system might sound daunting, but we are here to help.</p>

