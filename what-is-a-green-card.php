<h3>Family-Based Green Cards</h3>

<p>Family sponsorship is among the most common methods for foreign nationals to enter the United States. Green Card holders and American citizens can sponsor certain members of their family for legal status within the United States. The U.S. citizen or Green Card holder sponsoring their relatives must prove that they can financially support the beneficiary.</p>

<p>U.S. citizens and Green Card holders can file immigrant petitions on behalf of their wives, husbands, and unmarried children to gain legal entry for them into United States. U.S. citizens enjoy being able to sponsor wider categories of relatives than Green Card holders. US Citizens can sponsor parents, brothers, sisters, and married children. US Citizens must be over age 21 to sponsor parents, brothers, and sisters.</p>

<p>However, there are still limits to whom a legal citizen is allowed to sponsor. They are not allowed to sponsor aunts, uncles, grandparents, grandchildren, their in-laws, or their cousins—only close relatives and next of kin.</p>

